using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.ServiceModel;
using System.ServiceModel.Discovery;
using TicketingOffice.Common.Helpers;
using System.Xml;

namespace TicketingOffice.PricingBrokerService
